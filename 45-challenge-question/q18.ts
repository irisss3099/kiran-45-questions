//add countries name
let placetovisit:string[] = ["canada","turkey","america","switzerland","france"];

//print original array
console.log(placetovisit);

//print array in alphabetical order
console.log(placetovisit.sort());

//again print array in original state
console.log(placetovisit);

//print in reverse alphabetical order
console.log(placetovisit.sort().reverse());

//again print array in original state
console.log(placetovisit);

//array in reverse order
console.log(placetovisit.reverse());

//again print array in reverse, back to original
console.log(placetovisit.reverse());

//print array in alphabetical order
let sortedarray = ["canada","turkey","america","switzerland","france"]
sortedarray.sort();
console.log(sortedarray);

//print in reverse alphabetical order with statement
sortedarray.reverse();
